#계산기 만들기
def plus(a = 0, b = 0):
  print(a + b)

def minus(a = 0, b = 0):
  print(a - b)

def multiply(a = 0, b = 0):
  print(a * b)

def divide(a = 0, b = 1):
  print(a / b)

def power(a = 0, b = 1):
  print(a ** b)

plus(3, 2)
minus(4, 2)
multiply(1, 5)
divide(5, 4)
power(3, 2)

